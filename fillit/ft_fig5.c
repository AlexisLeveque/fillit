int		ft_fig5(char **grid, char letter, int y, int x)
{
	if (grid[y][x] == '.' && grid[y + 1][x] == '.' &&
			grid[y + 1][x + 1] == '.' && grid[y + 1][x + 2] == '.')
	{
		grid[y][x] = letter;
		grid[y + 1][x] = letter;
		grid[y + 1][x + 1] = letter;
		grid[y + 1][x + 2] = letter;
		return (0);
	}
	return (1);
}
