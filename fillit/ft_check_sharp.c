int	ft_check_sharp(char *block)
{
	int	i;
	int	sharp;

	i = 0;
	sharp = 0;
	while (i < 21)
	{
		if (block[i] == '#')
			sharp++;
		i++;
	}
	if (sharp != 4)
		return (1);
	return (0);
}
