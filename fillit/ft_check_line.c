#include "fillit.h"

int	ft_check_line(char *buf)
{
	if ((buf[0] != '#' && buf[0] != '.') || (buf[1] != '#' && buf[1] != '.')
	|| (buf[2] != '#' && buf[2] != '.')
	|| (buf[3] != '#' && buf[3] != '.') || buf[4] != '\n')
		return (1);
	return (0);
}
